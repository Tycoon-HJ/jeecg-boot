package org.jeecg.ysj.corecreatecode.template.interfaces;

public class InterfaceTemplate {
    private String pkg;
    private String url;
    private String interfaceName;
    private String interfaceObj;
    private String interfaceType;
    private String inputParam;
    private String inputParamObj;
    private String outputParam;
    private String inputParamBaseNum;
    private String outputParamBaseNum;

    public String getPkg() {
        return pkg;
    }

    public void setPkg(String pkg) {
        this.pkg = pkg;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getInterfaceName() {
        return interfaceName;
    }

    public void setInterfaceName(String interfaceName) {
        this.interfaceName = interfaceName;
    }

    public String getInterfaceObj() {
        return interfaceObj;
    }

    public void setInterfaceObj(String interfaceObj) {
        this.interfaceObj = interfaceObj;
    }

    public String getInputParamObj() {
        return inputParamObj;
    }

    public void setInputParamObj(String inputParamObj) {
        this.inputParamObj = inputParamObj;
    }

    public String getInterfaceType() {
        return interfaceType;
    }

    public void setInterfaceType(String interfaceType) {
        this.interfaceType = interfaceType;
    }

    public String getInputParam() {
        return inputParam;
    }

    public void setInputParam(String inputParam) {
        this.inputParam = inputParam;
    }

    public String getOutputParam() {
        return outputParam;
    }

    public void setOutputParam(String outputParam) {
        this.outputParam = outputParam;
    }

    public String getInputParamBaseNum() {
        return inputParamBaseNum;
    }

    public void setInputParamBaseNum(String inputParamBaseNum) {
        this.inputParamBaseNum = inputParamBaseNum;
    }

    public String getOutputParamBaseNum() {
        return outputParamBaseNum;
    }

    public void setOutputParamBaseNum(String outputParamBaseNum) {
        this.outputParamBaseNum = outputParamBaseNum;
    }
}
